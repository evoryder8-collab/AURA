import { addDays, formatISO, setHours, setMinutes, subDays } from 'date-fns'
import type { DemoAppointment, DemoClient, DemoState, DemoTherapist, MetricPoint } from './model'

const now = new Date()

const at = (dayOffset: number, hour: number, minute = 0) => {
  const day = dayOffset < 0 ? subDays(now, Math.abs(dayOffset)) : addDays(now, dayOffset)
  return formatISO(setMinutes(setHours(day, hour), minute))
}

const metricSeries = (
  prefix: string,
  pain: number[],
  stiffness: number[],
  rom: number[],
  ability: number[],
  events: Record<number, string[]> = {},
): MetricPoint[] =>
  pain.map((value, index) => ({
    id: `${prefix}-metric-${index + 1}`,
    appointmentId: `${prefix}-past-${index + 1}`,
    recordedAt: at(-42 + index * 7, 10),
    pain: value,
    stiffness: stiffness[index] ?? 0,
    rom: rom[index] ?? 0,
    function: ability[index] ?? 0,
    response: Math.min(10, Math.max(0, ability[index] ?? 0)),
    ...(events[index] ? { events: events[index] } : {}),
  }))

